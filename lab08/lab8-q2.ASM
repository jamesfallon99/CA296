DB 40
ORG 10

START:	;left hand side
 MOV BL, 30
 MOV AL, [BL]
 OUT 02
 
	;right hand side
 MOV CL, 60
 MOV AL, [CL]
 OUT 02

 CALL 80

LOOP:
 STI
 JMP LOOP

FINISH:
 HALT

ORG 80

RIGHTDIGIT:
 INC CL
 MOV AL, [CL]
 CMP AL, 00
 JZ LEFTDIGIT
 OUT 02
 JMP LOOPEND


LEFTDIGIT:
 INC BL
 MOV AL, [BL]
 CMP AL, 00
 JZ FINISH
 OUT 02
 MOV CL, 5F
 JMP RIGHTDIGIT


LOOPEND:
 RET

ORG 40
CLI
CALL 80
IRET

