FIRST_NUM:
 IN 00
 CMP AL,30; Hex for 0
 JS FIRST_NUM
 CMP AL,3A
 JNS FIRST_NUM
 SUB AL,30
 PUSH AL
 POP BL
 JMP SECOND_NUM

SECOND_NUM:
 IN 00
 CMP AL,30
 JS SECOND_NUM
 CMP AL,3A
 JNS SECOND_NUM
 SUB AL,30
 PUSH AL
 JMP MULTIPLY

MULTIPLY:
 MOV AL,00
 POP AL
 MUL AL,BL
 END
 